<h1>Chính sách riêng tư</h1>
<p>Chào mừng bạn đến với website bán laptop và linh kiện máy tính của chúng tôi. Chúng tôi tôn trọng và cam kết bảo vệ quyền riêng tư của bạn khi sử dụng dịch vụ của chúng tôi. Chính sách riêng tư này giải thích cách chúng tôi thu thập, sử dụng, lưu trữ và chia sẻ thông tin cá nhân của bạn khi bạn truy cập hoặc sử dụng website của chúng tôi. Xin vui lòng đọc kỹ chính sách này trước khi sử dụng website của chúng tôi.</p>
<h2>1. Thông tin cá nhân mà chúng tôi thu thập</h2>
<p>Thông tin cá nhân là thông tin có thể được sử dụng để xác định hoặc liên hệ với một cá nhân cụ thể, chẳng hạn như tên, địa chỉ, số điện thoại, email, số thẻ tín dụng hoặc thông tin thanh toán khác.</p>
<p>Chúng tôi có thể thu thập thông tin cá nhân của bạn khi bạn:</p>
<ul class = 'privacy_ul'>
<li>Đăng ký hoặc sử dụng tài khoản trên website của chúng tôi</li>
<li>Đặt hàng hoặc mua sản phẩm hoặc dịch vụ từ website của chúng tôi</li>
<li>Liên hệ với chúng tôi qua email, điện thoại, chat trực tuyến hoặc các phương tiện khác</li>
<li>Tham gia các khảo sát, cuộc thi, khuyến mãi hoặc các hoạt động do chúng tôi tổ chức</li>
<li>Đăng ký nhận bản tin, thông báo hoặc các ưu đãi từ chúng tôi</li>
<li>Cung cấp phản hồi, đánh giá hoặc bình luận về sản phẩm hoặc dịch vụ của chúng tôi</li>
<li>Sử dụng các tính năng xã hội trên website của chúng tôi, chẳng hạn như chia sẻ, bình luận hoặc like</li>
</ul>
<p>Thông tin cá nhân mà chúng tôi thu thập có thể bao gồm:</p>
<ul class = 'privacy_ul'>
<li>Tên, giới tính, ngày sinh và quốc tịch</li>
<li>Địa chỉ giao hàng và thanh toán</li>
<li>Số điện thoại và email</li>
<li>Thông tin thanh toán, bao gồm số thẻ tín dụng/debit, ngày hết hạn và mã bảo mật</li>
<li>Thông tin đơn hàng, bao gồm sản phẩm đã mua, số lượng, giá và trạng thái giao hàng</li>
<li>Thông tin liên lạc, bao gồm nội dung và thời gian của các cuộc gọi, email hoặc chat trực tuyến với chúng tôi</li>
</ul>
<h2>2. Cách chúng tôi sử dụng thông tin cá nhân của bạn</h2>
<p>Chúng tôi sử dụng thông tin cá nhân của bạn cho các mục đích sau:</p>
<ul class = 'privacy_ul'>
<li>Xử lý và giao hàng cho đơn hàng của bạn</li>
<li>Cung cấp dịch vụ hỗ trợ khách hàng và giải quyết các vấn đề liên quan đến đơn hàng, sản phẩm hoặc dịch vụ</li>
<li>Gửi cho bạn các thông báo, bản tin, ưu đãi hoặc thông tin về các sản phẩm hoặc dịch vụ mới của chúng tôi</li>
<li>Nâng cao chất lượng và tính năng của website và dịch vụ của chúng tôi</li>
<li>Phân tích hành vi và sở thích của bạn để hiểu rõ nhu cầu và mong muốn của bạn</li>
<li>Thực hiện các khảo sát, cuộc thi, khuyến mãi hoặc các hoạt động do chúng tôi tổ chức</li>
<li>Bảo vệ quyền và tài sản của chúng tôi và ngăn chặn gian lận, lạm dụng hoặc hành vi bất hợp pháp</li>
<li>Tuân thủ các quy định pháp lý, yêu cầu của cơ quan nhà nước hoặc các thỏa thuận hợp đồng</li>
</ul>
<h2>3. Cách chúng tôi chia sẻ thông tin cá nhân của bạn</h2>
<p>Chúng tôi không bán, cho thuê hoặc tiết lộ thông tin cá nhân của bạn cho bất kỳ bên thứ ba nào trừ khi có sự đồng ý của bạn hoặc như được quy định trong chính sách này.</p>
<p>Chúng tôi có thể chia sẻ thông tin cá nhân của bạn với các bên sau:</p>
<ul class = 'privacy_ul'>
<li>Các nhà cung cấp dịch vụ hoặc đối tác kinh doanh của chúng tôi, chẳng hạn như các công ty vận chuyển, thanh toán, lưu trữ, phân tích hoặc tiếp thị, để hỗ trợ chúng tôi trong việc cung cấp sản phẩm hoặc dịch vụ cho bạn. Các bên này chỉ được phép sử dụng thông tin cá nhân của bạn theo hướng dẫn của chúng tôi và cho các mục đích được chỉ định trong chính sách này.</li>
<li>Các bên thứ ba mà bạn đã đồng ý cho phép chúng tôi chia sẻ thông tin cá nhân của bạn, chẳng hạn như các trang web xã hội hoặc các ứng dụng khác mà bạn đã kết nối với website của chúng tôi.</li>
<li>Các bên thứ ba khi có yêu cầu pháp lý, quyền lợi hoặc an ninh. Chúng tôi có thể tiết lộ thông tin cá nhân của bạn khi có căn cứ để tin rằng việc làm như vậy là cần thiết để tuân thủ luật pháp </li>
</ul>
<h2>3. Cách chúng tôi bảo mật và lưu trữ thông tin cá nhân của bạn</h2>
<p>Chúng tôi coi trọng việc bảo mật và lưu trữ thông tin cá nhân của bạn một cách an toàn và hợp lý. Chúng tôi áp dụng các biện pháp kỹ thuật và tổ chức để ngăn chặn hoặc giảm thiểu rủi ro mất mát, truy cập trái phép, sử dụng sai mục đích, tiết lộ hoặc thay đổi thông tin cá nhân của bạn.</p>
<p>Một số biện pháp bảo mật mà chúng tôi sử dụng bao gồm:</p>
<ul class = 'privacy_ul'>
<li>Mã hóa thông tin cá nhân khi truyền qua internet</li>
<li>Sử dụng các phần mềm chống virus, tường lửa và các công cụ bảo mật khác</li>
<li>Giới hạn quyền truy cập vào thông tin cá nhân chỉ cho những nhân viên, đối tác hoặc bên thứ ba có nhu cầu hợp pháp</li>
<li>Đào tạo và nâng cao ý thức về bảo mật cho nhân viên và đối tác của chúng tôi</li>
<li>Thực hiện các kiểm tra và đánh giá định kỳ về hiệu quả của các biện pháp bảo mật</li>
</ul>
<p>Chúng tôi lưu trữ thông tin cá nhân của bạn trong các máy chủ được bảo vệ tại Việt Nam hoặc ở các quốc gia khác tuân thủ các tiêu chuẩn bảo mật tương đương. Chúng tôi chỉ lưu trữ thông tin cá nhân của bạn trong thời gian cần thiết để thực hiện các mục đích được nêu trong chính sách này hoặc theo quy định của pháp luật.</p>
<h2>4. Cách bạn có thể truy cập, cập nhật hoặc xóa thông tin cá nhân của bạn</h2>
<p>Bạn có quyền truy cập, cập nhật hoặc xóa thông tin cá nhân của bạn mà chúng tôi đang giữ. Bạn có thể thực hiện các yêu cầu này bằng cách:</p>
<ul class = 'privacy_ul'>
<li>Đăng nhập vào tài khoản của bạn trên website của chúng tôi và thay đổi các thông tin cá nhân trong phần cài đặt</li>
<li>Liên hệ với chúng tôi qua email, điện thoại, chat trực tuyến hoặc các phương tiện khác để yêu cầu xem, sửa đổi hoặc xóa thông tin cá nhân của bạn</li>
<li>Hủy đăng ký nhận bản tin, thông báo hoặc các ưu đãi từ chúng tôi bằng cách nhấp vào liên kết hủy đăng ký ở cuối mỗi email hoặc gửi yêu cầu cho chúng tôi</li>
</ul>
<p>Chúng tôi sẽ xử lý yêu cầu của bạn trong thời gian sớm nhất có thể và tuân thủ các quy định
